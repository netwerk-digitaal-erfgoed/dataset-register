// Derives a stable structural fingerprint of the DatasetAnalysis type via the
// TypeScript compiler (which resolves the ldkit SchemaInterface<…> computed
// types that ts-json-schema-generator cannot). The hash versions the Valkey
// cache key: a shape change → new hash → old blobs are ignored and TTL-expire,
// so a new app version never reads a structurally-incompatible cached blob.
// Regenerate with `npm run gen:analysis-schema-hash`; a CI check keeps it current.
import tsmod from 'typescript';
import { createHash } from 'node:crypto';
import { writeFileSync } from 'node:fs';
const ts = tsmod.default || tsmod;

const file = 'src/lib/services/dataset-detail.ts';
const program = ts.createProgram([file], {
  target: ts.ScriptTarget.ESNext,
  module: ts.ModuleKind.ESNext,
  moduleResolution: ts.ModuleResolutionKind.Bundler,
  strict: true,
  skipLibCheck: true,
  paths: { '$env/static/public': ['/dev/null'] },
  noEmit: true,
});
const checker = program.getTypeChecker();
const sf = program.getSourceFile(file);
let decl;
(function visit(n) {
  if (ts.isInterfaceDeclaration(n) && n.name.text === 'DatasetAnalysis')
    decl = n;
  ts.forEachChild(n, visit);
})(sf);
if (!decl) {
  console.error('DatasetAnalysis not found');
  process.exit(1);
}

const seen = new Set();
function describe(type, depth) {
  if (depth > 12) return '…';
  if (type.isUnion?.())
    return (
      '(' +
      type.types
        .map((t) => describe(t, depth))
        .sort()
        .join('|') +
      ')'
    );
  const f = type.flags;
  if (f & ts.TypeFlags.StringLike) return 'string';
  if (f & ts.TypeFlags.NumberLike) return 'number';
  if (f & ts.TypeFlags.BooleanLike) return 'boolean';
  if (f & ts.TypeFlags.Null) return 'null';
  if (f & (ts.TypeFlags.Undefined | ts.TypeFlags.Void)) return 'undefined';
  if (checker.isArrayType(type))
    return describe(checker.getTypeArguments(type)[0], depth + 1) + '[]';
  const id = type.id;
  if (id && seen.has(id)) return 'circular';
  if (id) seen.add(id);
  const props = checker.getPropertiesOfType(type);
  if (!props.length) return checker.typeToString(type);
  const body = props
    .map(
      (p) =>
        p.name +
        ':' +
        describe(checker.getTypeOfSymbolAtLocation(p, decl), depth + 1),
    )
    .sort()
    .join(',');
  if (id) seen.delete(id);
  return '{' + body + '}';
}

const fingerprint = describe(checker.getTypeAtLocation(decl.name), 0);
const hash = createHash('sha256')
  .update(fingerprint)
  .digest('hex')
  .slice(0, 12);
const out = 'src/lib/services/dataset-analysis-schema-hash.ts';
writeFileSync(
  out,
  `// GENERATED by scripts/gen-analysis-schema-hash.mjs — do not edit by hand.
// Structural fingerprint of the DatasetAnalysis type; versions the analysis
// cache key so a shape change automatically invalidates stale cached blobs.
// Run \`npm run gen:analysis-schema-hash\` after changing DatasetAnalysis.
export const DATASET_ANALYSIS_SCHEMA_HASH = '${hash}';
`,
);
console.log('fingerprint:', fingerprint.length, 'chars → hash', hash);
console.log('wrote', out);
